#include "ft_printf.h"

int		check_spec(char **s, t_parse *e)
{
	if (**s == 's' || **s == 'S' || **s == 'p' || **s == 'd'
	|| **s == 'D' || **s == 'i' || **s == 'o'
	|| **s == 'O' || **s == 'u' || **s == 'U'
	|| **s == 'x' || **s == 'X' || **s == 'c'
	|| **s == 'C')
	{
		e->spec = ft_strndup(*s, 1);
		(*s)++;
		return (1);
	}
	else
		return (0);
}
