#include "ft_printf.h"

void	check_len(char **s, t_parse *e)
{
	char	*tmp;

	tmp = *s;
	if ((*tmp == 'h' && *(tmp + 1) == 'h')
	|| (*tmp == 'l' && *(tmp + 1) == 'l'))
	{
		e->len = ft_strndup(*s, 2);
		*s = *s + 2;
	}
	else if (**s == 'h' || **s == 'l' || **s == 'j'
	|| **s == 'z')
	{
		e->len = ft_strndup(*s, 1);
		(*s)++;
	}
}
